#include <err.h>
#include <stdio.h>
#include <string.h>

/* OP-TEE TEE client API (built by optee_client) */
#include <tee_client_api.h>
#define TA_HELLO_WORLD_UUID \
	{ 0x8aaaf200, 0x2450, 0x11e4, \
		{ 0xab, 0xe2, 0x00, 0x02, 0xa5, 0xd5, 0xc5, 0x1b} }

/* The function IDs implemented in this TA */
#define TA_HELLO_WORLD_CMD_INC_VALUE		0
#define TA_HELLO_WORLD_CMD_DEC_VALUE		1
#define TA_RANDOM_UUID \
	{ 0xb6c53aba, 0x9669, 0x4668, \
		{ 0xa7, 0xf2, 0x20, 0x56, 0x29, 0xd0, 0x0f, 0x86} }

/* The function ID implemented in this TA */
#define TA_RANDOM_CMD_GENERATE		0
/* To the the UUID (found the the TA's h-file(s)) */

int main(int argc, char *argv[])
{
	TEEC_Result res;
	TEEC_Context ctx;
//	TEEC_Context ctx;
	TEEC_Session sess1;
	TEEC_Session sess2;
	TEEC_Operation op;
	TEEC_Operation op1 = { 0 };
	TEEC_UUID uuid1 = TA_HELLO_WORLD_UUID;
	TEEC_UUID uuid2 =TA_RANDOM_UUID;
	uint8_t random_uuid[16] = { 0 };
	int i;
	uint32_t err_origin;

	/* Initialize a context connecting us to the TEE */
	res = TEEC_InitializeContext(NULL, &ctx);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_InitializeContext failed with code 0x%x", res);
/*	res = TEEC_InitializeContext(NULL, &ctx1);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_InitializeContext failed with code 0x%x", res);
*/
	/*
	 * Open a session to the "hello world" TA, the TA will print "hello
	 * world!" in the log when the session is created.
	 */
	res = TEEC_OpenSession(&ctx, &sess1, &uuid1,
			       TEEC_LOGIN_PUBLIC, NULL, NULL, &err_origin);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_Opensession failed with code 0x%x origin 0x%x",
			res, err_origin);
	

	/*
	 * Execute a function in the TA by invoking it, in this case
	 * we're incrementing a number.
	 *
	 * The value of command ID part and how the parameters are
	 * interpreted is part of the interface provided by the TA.
	 */

	/* Clear the TEEC_Operation struct */
	memset(&op, 0, sizeof(op));

	/*
	 * Prepare the argument. Pass a value in the first parameter,
	 * the remaining three parameters are unused.
	 */
	op.paramTypes = TEEC_PARAM_TYPES(TEEC_VALUE_INOUT, TEEC_NONE,
					 TEEC_NONE, TEEC_NONE);
	op.params[0].value.a = 42;

	/*
	 * TA_HELLO_WORLD_CMD_INC_VALUE is the actual function in the TA to be
	 * called.
	 */
	printf("Invoking TA to increment %d\n", op.params[0].value.a);
	res = TEEC_InvokeCommand(&sess1, TA_HELLO_WORLD_CMD_INC_VALUE, &op,
				 &err_origin);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_InvokeCommand failed with code 0x%x origin 0x%x",
			res, err_origin);
	printf("TA incremented value to %d\n", op.params[0].value.a);

	/*
	 * We're done with the TA, close the session and
	 * destroy the context.
	 *
	 * The TA will print "Goodbye!" in the log when the
	 * session is closed.
	 */

	TEEC_CloseSession(&sess1);
	res = TEEC_OpenSession(&ctx, &sess2, &uuid2,
			       TEEC_LOGIN_PUBLIC, NULL, NULL, &err_origin);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_Opensession failed with code 0x%x origin 0x%x",
			res, err_origin);

	memset(&op1, 0, sizeof(op1));
	op1.paramTypes = TEEC_PARAM_TYPES(TEEC_MEMREF_TEMP_OUTPUT,
					 TEEC_NONE, TEEC_NONE, TEEC_NONE);
	op1.params[0].tmpref.buffer = random_uuid;
	op1.params[0].tmpref.size = sizeof(random_uuid);

	printf("Invoking TA to generate random UUID... \n");
	res = TEEC_InvokeCommand(&sess2, TA_RANDOM_CMD_GENERATE,
				 &op1, &err_origin);
	if (res != TEEC_SUCCESS)
		errx(1, "TEEC_InvokeCommand failed with code 0x%x origin 0x%x",
			res, err_origin);

	printf("TA generated UUID value = 0x");
	for (i = 0; i < 16; i++)
		printf("%x", random_uuid[i]);
	printf("\n");

	TEEC_CloseSession(&sess2);
//TEEC_CloseSession(&sess1);
	
	TEEC_FinalizeContext(&ctx);
	
//	TEEC_FinalizeContext(&ctx);

	return 0;
}
